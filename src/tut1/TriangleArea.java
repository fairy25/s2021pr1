package tut1;

public class TriangleArea {
    public static void main(String[] args) {
        double b = 3;
        double hb = 1.5;
        System.out.println("The triangle's base is " + b + " (cm) and height is " + hb + "(cm).");
        double area = b * hb / 2;
        System.out.println("Its area (cm2) is:");
        System.out.println(area);
    }
}

